#include "libft.h"

int	ft_putnbr(int n)
{
	int	digit;

	digit = 0;
	if (n == -2147483648)
			return (ft_putstr("-2147483648"));
	else
	{
		if (n < 0)
		{
			digit = ft_putchar('-');
			n = n * (-1);
		}
		if (n > 9)
		{
			digit =	ft_putnbr(n / 10);
			digit = (n % 10) + '0';
		}
	}
	return (digit);	
}
