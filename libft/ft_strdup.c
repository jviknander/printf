#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*m;
	int		i;

	m = malloc(ft_strlen(s1) + 1);
	if (!m)
		return (NULL);
	i = -1;
	while (s1[++i])
		m[i] = s1[i];
	m[i] = '\0';
	return (m);
}
